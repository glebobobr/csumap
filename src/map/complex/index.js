const TRANSITION_TYPES = ['passage', 'transition', 'corridor']

export function processBuildingsToFeatures(geojson) {
  return {
    type: 'FeatureCollection',
    features: geojson.features
      .filter(f => f.geometry?.type === 'Polygon')
      .map(f => {
        const p = f.properties || {}

        const part = new BuildingPart({
          id: p.id || f.id,
          name: p.name || 'Здание',
          type: TRANSITION_TYPES.includes(p.type) ? 'transition' : 'building',
          coordinates: f.geometry.coordinates[0],
          height: p.height || (p.levels || 1) * (p.floor_height || 3.5),
          baseHeight: p.base_height || 0,
          color: p.color || '#CCCCCC',
          description: p.description || '',
          levels: p.levels || 1
        })

        const feature = part.toFeature()
        if (p.complex_id) {
          feature.properties.complex_id = p.complex_id
        }
        return feature
      })
  }
}
